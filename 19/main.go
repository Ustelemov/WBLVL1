package main

import "fmt"

func reverseString(in string) string {
	runes := []rune(in) //–ø–æ–ª—É—á–∞–µ–º —Å–ª–∞–π—Å —Ä—É–Ω, –∫–æ–¥–∏—Ä—É—é—â–∏—Ö –æ–¥–∏–Ω —Å–∏–º–≤–æ–ª –≤ unicode
	n := len(runes)

	for i := 0; i < n/2; i++ {
		runes[i], runes[n-i-1] = runes[n-i-1], runes[i]
	}

	out := string(runes) //–ø–µ—Ä–µ–≤–æ–¥–∏–º —Å–ª–∞–π—Å —Ä—É–Ω –≤ —Å—Ç—Ä–æ–∫—É
	return out
}

